#include <stdio.h>

int main(){
    printf("Data types in c\n");

    // integer data types 
    int number = 10;//integer data types prints whole integer number without decimal.
    printf("Integer number is : %d\n", number);
    // Floating point number data types 
    float flNumber = 10.56;//float data types store 6-7 decimal number;
    printf("Floating point number data types : %f\n", flNumber);
    // dobule data types 
    double piNum = 3.1416;//double data types store 15 decimal number
    printf("Dobule data types: %lf\n", piNum);
    // Character data types 
    char result = 'F';
    printf("Character type data types: %c", result);

    return 0;
}